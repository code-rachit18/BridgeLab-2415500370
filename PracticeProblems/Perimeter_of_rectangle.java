class Perimeter_of_recyangle {
    public static void main(String[] args) {
        double length = 10.0;
        double width = 5.0;
        double perimeter = 2 * (length + width);
        System.out.println("Perimeter of the rectangle with length " + length + " and width " + width + " is: " + perimeter);
    }
}
